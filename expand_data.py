def replace_array(html, array_name, new_content):
    """Find 'const ARRAY_NAME = [' and replace its content up to '];'"""
    pattern = re.compile(
        r'(const ' + re.escape(array_name) + r'\s*=\s*\[)'
        r'([\s\S]*?)'
        r'(\n\];)'
    )
    match = pattern.search(html)
    if not match:
        print(f"   ⚠️  {array_name}: NOT FOUND")
        return html
    replacement = match.group(1) + '\n' + ',\n'.join(new_content) + '\n' + match.group(3)
    return html[:match.start()] + replacement + html[match.end():]


