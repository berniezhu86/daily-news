"""Safe replacement of JS data arrays in index.html - manual JS formatting, no json.dumps"""
import re

Q = {'L': '\\u201C', 'R': '\\u201D'}

def j(val):
    """JS-safe string quoting using unicode escapes for Chinese quotes"""
    return '"' + val.replace('\u201C', '\\u201C').replace('\u201D', '\\u201D') + '"'

def fmt_arr(items, indent=2):
    """Format array of objects as JS source"""
    out = []
    sp = ' ' * indent
    for i, item in enumerate(items):
        comma = ',' if i < len(items) - 1 else ''
        if isinstance(item, dict):
            out.append(sp + '{')
            props = []
            for k, v in item.items():
                if isinstance(v, bool):
                    props.append(f'{k}:{str(v).lower()}')
                elif isinstance(v, (int, float)):
                    props.append(f'{k}:{v}')
                else:
                    props.append(f'{k}:{j(v)}')
            out[-1] += ', '.join(props) + ' }' + comma
        else:
            out.append(sp + j(item) + comma)
    return '\n'.join(out)

def replace_in_file(html, var_name, data):
    """Replace const var_name = [...]; with new data"""
    start = f'const {var_name} = ['
    idx = html.find(start)
    if idx == -1:
        print(f'  SKIP {var_name} (not found)')
        return html
    # Find matching ]; by counting brackets from first [
    bracket_pos = html.index('[', idx)
    depth = 0
    for i in range(bracket_pos, len(html)):
        if html[i] == '[':
            depth += 1
        elif html[i] == ']':
            depth -= 1
            if depth == 0:
                # Verify ; follows
                end = i + 2 if i + 1 < len(html) and html[i+1] == ';' else i + 1
                new_content = start + '\n' + fmt_arr(data) + '\n];'
                return html[:idx] + new_content + html[end:]
    print(f'  FAIL {var_name} (unbalanced brackets)')
    return html

